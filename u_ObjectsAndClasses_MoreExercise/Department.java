package u_ObjectsAndClasses_MoreExercise;

public class Department {
    private String departmentName;
    private double totalSalary;
    private int departmentCounter;

    public Department() {
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName;
    }

    public double getTotalSalary() {
        return totalSalary;
    }

    public void setTotalSalary(double totalSalary) {
        this.totalSalary = totalSalary;
    }

    public int getDepartmentCounter() {
        return departmentCounter;
    }

    public void setDepartmentCounter(int departmentCounter) {
        this.departmentCounter = departmentCounter;
    }
}
